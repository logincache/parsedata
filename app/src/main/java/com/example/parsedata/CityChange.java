public class CityChange {
    public CityChange(String list_city) {
        if (list_city.equals("Петрозаводск")){
            list_city.equals("petrozavodsk");

        }
        if (list_city.equals("Санкт-Петербург")){
            list_city.equals("sankt-peterburg");
        }
        if (list_city.equals("Москва")){
            list_city.equals("moskva");
        }
        if (list_city.equals("Краснодар")){
            list_city.equals("krasnodar");
        }

       return;
    }




}
